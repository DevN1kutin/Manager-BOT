# ─── TIME PARSER ────────────────────────────────────────────────────────────────

def parse_time(time_str: str):
    """Parse time string like 30s, 5m, 2h, 1d → (seconds, readable)"""
    if not time_str:
        return None, "навсегда"

    units = {"s": 1, "с": 1, "m": 60, "м": 60, "h": 3600, "ч": 3600, "d": 86400, "д": 86400}
    unit = time_str[-1].lower()
    if unit in units:
        try:
            value = int(time_str[:-1])
            seconds = value * units[unit]
            labels = {"s": "сек", "с": "сек", "m": "мин", "м": "мин",
                      "h": "ч", "ч": "ч", "d": "дн", "д": "дн"}
            return seconds, f"{value} {labels[unit]}"
        except ValueError:
            pass
    try:
        return int(time_str), f"{time_str} сек"
    except:
        return None, "навсегда"


