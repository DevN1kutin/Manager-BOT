# ─── SHIP ───────────────────────────────────────────────────────────────────────

def ship_users(name1: str, name2: str) -> str:
    score = random.randint(0, 100)
    bar = "❤️" * (score // 10) + "🖤" * (10 - score // 10)
    if score >= 90:
        verdict = "Идеальная пара! 😍"
    elif score >= 70:
        verdict = "Хорошая совместимость! 😊"
    elif score >= 50:
        verdict = "Могут сработаться 🤔"
    elif score >= 30:
        verdict = "Непростые отношения 😅"
    else:
        verdict = "Им лучше держаться подальше 💀"

    half1 = name1[:len(name1)//2]
    half2 = name2[len(name2)//2:]
    ship_name = half1 + half2

    return (
        f"💕 <b>Шипперим!</b>\n\n"
        f"👤 {html.escape(name1)}\n"
        f"💝 + 💝\n"
        f"👤 {html.escape(name2)}\n\n"
        f"[{bar}]\n"
        f"❤️ Совместимость: <b>{score}%</b>\n"
        f"🚢 Шип-имя: <b>{html.escape(ship_name)}</b>\n"
        f"💬 {verdict}"
    )


