# ─── ADMIN CHECK ────────────────────────────────────────────────────────────────

async def is_admin(bot: Bot, message: Message) -> bool:
    if message.chat.type == "private":
        return True
    try:
        member = await bot.get_chat_member(message.chat.id, message.from_user.id)
        return member.status in ("administrator", "creator")
    except:
        return False


